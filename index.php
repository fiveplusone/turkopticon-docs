
    <p>
      This is docs.turkopticon.info, the documentation site for <a href="http://turkopticon.ucsd.edu">Turkopticon</a>.
    </p>

    <p>
      Turkopticon is a web application and browser add-on for reviewing employers (&ldquo;requesters&rdquo;) on <a href="http://mturk.com">Amazon Mechanical Turk</a>.
    </p>

